people = 20
cats = 10
dogs = 10

if people < cats:
    print("Too many cats, it's a meowcalypse")

if people > cats:
    print("Too many people, it's a meowpamper time !")

if people < dogs:
    print("Too many dogs, it's woof-woof everywhere !")

if people > dogs:
    print("Too many people, it's a sad day for many dogless people !")

dogs += 10

if people >= dogs:
    print("People are greater than or equal to dogs. Yeah ?")

if people <= dogs:
    print("Less people and many dogs")

if people == dogs:
    print("People are dogs, indeed!")
